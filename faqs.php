<hr>

<div class="line">&nbsp;</div>

<p class="bold"><span class="greentext">Q:</span> My tree has fungi growing on it.  Does this mean the tree has to be removed?</p>
<p><span class="greentext">A:</span> No, not necessarily. Removing a tree should always be a last resort.  We look at a number of factors when assessing a tree, which is showing signs of decay. After assessing these factors, we will recommend a course of action which we believe to be suitable. This may include further assessment of the decay using specialist equipment. Based upon these findings, we will recommend only necessary remedial work, which could mean a crown reduction or removal of branches that are cause for concern. In some circumstances this could also include the removal of the tree if the decay is too advanced.</p>
<hr>
<div class="line">&nbsp;</div>

<p class="bold"><span class="greentext">Q:</span> We have a large tree in our garden that we want removing. How would you do this?</p>
<p><span class="greentext">A:</span> When large trees cannot be felled, we climb and dismantle them with the aid of a rope and harness. Specialist equipment (when required) is used to lower the branches to the ground to avoid any damage to the surrounding area.</p>
<hr>
<div class="line">&nbsp;</div>

<p class="bold"><span class="greentext">Q:</span> My neighbour has a tree which overhangs my garden. Am I allowed to cut off the offending branches?</p>
<p><span class="greentext">A:</span> The answer is yes. However, this all depends on whether the tree is protected or in a conservation area. If this is not the case, removal of limbs should be cut back to the boundary line using correct pruning cuts. The removal of large branches should only be carried out by qualified professionals in the interest of safety. Any work carried out should not be to the detriment of the tree. In the interest of being a good neighbour, we would always recommend you discuss the problem with the owner of the tree before undertaking any work.  If you are lucky your neighbour may even contribute to the cost...</p>
<hr>

<div class="line">&nbsp;</div>

