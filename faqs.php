<h2 id="faq">Frequently asked questions</h2>
<hr>

<p class="bold"><span class="greentext">Q:</span> My tree has a protection order on it? Do I need permission before work is carried out?</p>
<p><span class="greentext">A:</span> Yes. If your tree has a protection order on it or if you live in a conservation area, an application needs to be made to the local authority seeking permission for the work. This rule also applies if you are looking to remove the tree.  However, permission is not usually required for the removal of deadwood, snapped branches or dead trees.We regularly speak with many different authorities on this matter and can carry out the application process for you at no extra cost.</p>
<hr>
<div class="line">&nbsp;</div>

